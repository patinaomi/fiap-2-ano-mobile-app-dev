// Exercício: Inversão de Array

fun main() {

    // Array Original
    val originalArray = arrayOf(1,2,3,4,5)
    val reversedArray = originalArray.reversed()

    // Impressão dos Arrays
    println("Array original: ${originalArray.joinToString()}")
    println("Array invertido: ${reversedArray.joinToString()}")

    // Exemplo de como é utilizado de A - Z, ou Z - A
}